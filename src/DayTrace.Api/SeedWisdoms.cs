    public static async Task SeedWisdomsAsync(this WebApplication app)
    {
        using var scope = app.Services.CreateScope();
        var wisdomRepo = scope.ServiceProvider.GetRequiredService<IWisdomRepository>();
        var logger = scope.ServiceProvider.GetRequiredService<ILogger<Program>>();

        var count = await wisdomRepo.CountAsync();
        if (count > 0)
        {
            return; // Already seeded
        }

        var now = DateTime.UtcNow;
        var wisdoms = GetWisdoms(now);

        try
        {
            await wisdomRepo.AddRangeAsync(wisdoms);
            logger.LogInformation("Seeded {Count} wisdoms", wisdoms.Count);
        }
        catch (Exception ex)
        {
            logger.LogError(ex, "Failed to seed wisdoms");
        }
    }

