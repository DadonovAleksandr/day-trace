    private static void AddPhilosophy(
        List<Wisdom> wisdoms,
        DateTime createdAt,
        params (string text, string author)[] items)
    {
        foreach (var (text, author) in items)
        {
            wisdoms.Add(new Wisdom
            {
                Text = text,
                Category = "philosophy",
                Author = author,
                CreatedAt = createdAt
            });
        }
    }

    private static void AddScience(
        List<Wisdom> wisdoms,
        DateTime createdAt,
        params string[] items)
    {
